window.findItem = function(name) {
  const q = name.toLowerCase();
  return window.ITEMS_ALL.find(i=>i.name.toLowerCase()===q)
      || window.ITEMS_ALL.find(i=>i.name.toLowerCase().includes(q));
};
window.findItemById = function(id) {
  return window.ITEMS_ALL.find(i=>i.id===id)
      || window.ITEMS_KITS.find(k=>k.id===id);
};
